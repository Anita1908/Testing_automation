# Шаг 1: объявление функции проверки високосного года
def is_year_leap(year):
    return year % 4 == 0

# Шаг 2: выбираем произвольный год для тестирования
test_year = 2023

# Шаг 3: сохранение результата вызова функции в переменную
result = is_year_leap(test_year)

# Шаг 4: вывод результата в нужной форме
print(f"Год {test_year}: {result}")
